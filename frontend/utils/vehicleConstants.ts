/**
 * دریافت مدل‌های یک برند بر اساس نوع خودرو
 */
export function getModelsForBrand(vehicleType: string, brand: string): Record<string, string[]> {
  if (vehicleType === 'خودرو سنگین') {
    return heavyVehicleBrands[brand] || {};
  } else if (vehicleType === 'خودرو نیمه سنگین') {
    return mediumVehicleBrands[brand] || {};
  } else if (vehicleType === 'سواری') {
    return carBrands[brand] || {};
  } else if (vehicleType === 'وانت') {
    return pickupBrands[brand] || {};
  }
  return {};
}

/**
 * دریافت همه برندها بر اساس نوع خودرو
 */
export function getBrandsForType(vehicleType: string): string[] {
  if (vehicleType === 'خودرو سنگین') {
    return Object.keys(heavyVehicleBrands);
  } else if (vehicleType === 'خودرو نیمه سنگین') {
    return Object.keys(mediumVehicleBrands);
  } else if (vehicleType === 'سواری') {
    return Object.keys(carBrands);
  } else if (vehicleType === 'وانت') {
    return Object.keys(pickupBrands);
  }
  return [];
}

